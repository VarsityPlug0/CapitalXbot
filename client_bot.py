async def client_bot_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """Handle the /clientbot command to launch the enhanced client assistance feature."""
    try:
        user = update.effective_user
        if not user:
            return
            
        # Add user to database
        from database import add_user
        add_user(
            chat_id=user.id, 
            username=user.username, 
            first_name=user.first_name, 
            last_name=user.last_name
        )
        
        # Log command
        from database import log_command
        log_command(user.id, "/clientbot")
        
        # Greet user and show main menu
        greeting = f"üëã Hello {user.first_name or 'there'}!\n\n"
        greeting += "I'm your CapitalX Assistant, here to help you navigate our platform and answer your questions.\n\n"
        greeting += "How can I help you today?"
        
        keyboard = [
            [InlineKeyboardButton("üí≥ Deposit Process", callback_data="clientbot_deposit")],
            [InlineKeyboardButton("üì§ Withdrawal Process", callback_data="clientbot_withdraw")],
            [InlineKeyboardButton("üìà Investment Plans", callback_data="clientbot_investments")],
            [InlineKeyboardButton("üí∞ Wallet Management", callback_data="clientbot_wallet")],
            [InlineKeyboardButton("üë• Referral Program", callback_data="clientbot_referral")],
            [InlineKeyboardButton("üß≠ Navigation Help", callback_data="clientbot_navigation")],
            [InlineKeyboardButton("‚ùì Ask a Question", callback_data="clientbot_ask")]
        ]
        
        reply_markup = InlineKeyboardMarkup(keyboard)
        
        if update.message:
            await update.message.reply_text(greeting, reply_markup=reply_markup)
        logger.info(f"Client bot command handled for user {user.id}")
        
    except Exception as e:
        logger.error(f"Error in client_bot_command: {e}")
        # Try to send error message but don't fail if we can't
        try:
            if update.effective_chat:
                await context.bot.send_message(
                    chat_id=update.effective_chat.id,
                    text="Sorry, something went wrong. Please try again."
                )
        except:
            pass

